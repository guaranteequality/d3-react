    var pinlength = 20;
    var subbands = [];
    for (let i = 0; i < bands; i++) {
        let pins = new Array(pinlength);
        for (let j = 0; j < pinlength; j++) {
            pins[j] = signal[i*pinlength+j];
        }
        subbands.push(pins);
    }

    return [pureSignal, signal, subbands];
